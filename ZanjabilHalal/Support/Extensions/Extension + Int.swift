import Foundation

extension Int16 {
    
    var toDecimal: Decimal {
        return Decimal(self)
    }
    var toString: String {
        return String(self)
    }
}

extension Int {
    
    var toDecimal: Decimal {
        return Decimal(self)
    }
    var toString: String {
        return String(self)
    }
}
